package com.example.mcpserver.dto.common.enums;

/**
 * Skill proficiency levels for candidate profiles.
 */
public enum SkillLevel {
    /**
     * 0-1 years or learning
     */
    BEGINNER,

    /**
     * 1-3 years, can work independently on guided tasks
     */
    INTERMEDIATE,

    /**
     * 3-7 years, can design and implement complex solutions
     */
    ADVANCED,

    /**
     * 7+ years, recognized subject matter expert, mentors others
     */
    EXPERT
}
